package day19_loops;

public class DoWhileRunsOnce {

	public static void main(String[] args) {
		// in this example do while loop body code will run once
		//then test the condtion then exti the loop.
		//condition is false.
		int count =10;
		do {
			System.out.println("count: "+count);
			count++;
		}while(count<5);
		
		
	}

}
