package day03_helloworld_variables;

public class VariableDeclaration {
	public static void main(String[] args) {
		//declare single variable
		int apples;
		//declare multiple variables in single statement
		int bananas;
		int orange, mango;
		
		//declare variables and assign value in single statement
		int kiwi=14;
		System.out.println(kiwi);
		//declare variables and assign value in single statement
          // in single statement
		int watermelon = 11, grapes = 100, cherries = 12, apricots; 
	}

}
